stt = 0.05;
Tfinal = 20;

s11 = tf(1, [6, 1]);
s12 = tf(10, [2, 1]);

s21 = zpk([], [-1/2 -99/2], 250);
s22 = tf(10, [1/25 1/5 1]);

% Step for part 1
info11 = stepinfo(s11, "SettlingTimeThreshold", stt);
info12 = stepinfo(s12, "SettlingTimeThreshold", stt);

[y11, t11] = step(s11, Tfinal);
[y12, t12] = step(s12, Tfinal);

results1 = table(...
    ["s11(t)"              ; "s12(t)"], ...
    [18                   ; 6], ...
    [info11.SettlingTime  ; info12.SettlingTime], ...
    [0                    ; -9], ...
    [1 - y11(end)         ; 1 - y12(end)], ...
    [pole(s11)            ; pole(s12)], ...
    VariableNames=["Tf"; "Est: ST"; "Settling time"; "Est: SE"; "Static error"; "Pôles"] ...
);

disp("Step info part 1:");
disp(results1);

% Step for part 1
info21 = stepinfo(s21, "SettlingTimeThreshold", stt);
info22 = stepinfo(s22, "SettlingTimeThreshold", stt);

[y21, t21] = step(s21, Tfinal);
[y22, t22] = step(s22, Tfinal);

results2 = table(...
    ["s21(t)"                       ; "s22(t)"], ...
    [6.03                           ; 1.05], ...
    [info21.SettlingTime            ; info22.SettlingTime], ...
    [-9.1                           ; -9], ...
    [1 - y21(end)                   ; 1 - y22(end)], ...
    ["-49.5;-0.5"                   ; "-2.5-4.3301i;-2.5+4.3301i"], ...
    [strjoin(string(pole(s21)), ","); strjoin(string(pole(s22)), ",")], ...
    [0                              ; 0.16], ...
    [info21.Overshoot/100           ; info22.Overshoot/100], ...
    VariableNames=["Tf"; "Est: ST"; "Settling time"; "Est: SE"; "Static error"; "Est: Pôles"; "Pôles"; "Est: OS"; "Overshoot"] ...
);

disp("Step info part 2:");
disp(results2);

% Impulse part 1&2
stepplot(s11, s12)
stepplot(s21, s22)